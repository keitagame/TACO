.section .text.boot
.code32
.global switch_to_long_mode
switch_to_long_mode:
    # -------------------------------------------------------------
    # 1. ページテーブルのベースをCR3にロード
    # -------------------------------------------------------------
    movl $page_table_l4, %eax     # 下記で定義したPML4のアドレス
    movl %eax, %cr3

    # -------------------------------------------------------------
    # 2. CR4レジスタでPAE（物理アドレス拡張）を有効化
    # -------------------------------------------------------------
    movl %cr4, %eax
    orl $(1 << 5), %eax           
    movl %eax, %cr4

    # -------------------------------------------------------------
    # 3. IA32_EFER MSRでロングモード（LME）を有効化
    # -------------------------------------------------------------
    movl $0xC0000080, %ecx        
    rdmsr                         
    orl $(1 << 8), %eax           
    wrmsr                         

    # -------------------------------------------------------------
    # 4. CR0レジスタでページング（PG）を有効化
    # -------------------------------------------------------------
    movl %cr0, %eax
    orl $(1 << 31), %eax          
    movl %eax, %cr0

    # -------------------------------------------------------------
    # 5. 64ビット記述子を含むGDTをロード
    # -------------------------------------------------------------
    lgdt gdt_pointer              # 下記で定義したGDTポインタ

    # -------------------------------------------------------------
    # 6. ロングジャンプで完全に64ビット環境（ロングモード）へ遷移
    # -------------------------------------------------------------
    ljmp $0x08, $entry_64bit

.code64
entry_64bit:
    xorw %ax, %ax
    movw %ax, %ds
    movw %ax, %es
    movw %ax, %ss
    movabs $0x1122334455667788, %rax
    hlt

# =============================================================================
# データ定義セクション (32ビット環境でもビルドできるよう修正)
# =============================================================================
.section .data.boot
.align 4096

# -----------------------------------------------------------------------------
# 最小限の初期ページテーブル (4KBアライメントが必須)
# -----------------------------------------------------------------------------
# 1. PML4 (Page Map Level 4)
.global page_table_l4
page_table_l4:
    # 0x00000000_00000000 用（アイデンティティマッピング用）
    # .quad ラベル の代わりに .long ラベル, 0 と分けることで32bitリロケーションにします
    .long page_table_pdpt + 0x003  # 下位32ビット（アドレス + フラグ）
    .long 0                        # 上位32ビット（0固定）
    
    .fill 510, 8, 0                # 空のエントリ（510個 × 8バイト）
    
    # 0xFFFFFFFF_80000000 用（Higher-Half用：高位アドレス）
    .long page_table_pdpt + 0x003  # 下位32ビット
    .long 0                        # 上位32ビット

# 2. PDPT (Page Directory Pointer Table)
page_table_pdpt:
    .long page_table_pd + 0x003    # 下位32ビット
    .long 0                        # 上位32ビット
    .fill 511, 8, 0

# 3. PD (Page Directory) - 2MBの巨大ページ(Huge Page)を使って1GB分をマッピング
page_table_pd:
    # 0x083 = Present + Read/Write + PageSize(2MB巨大ページフラグ)
    .long 0x00000000 + 0x083, 0    # 0MB ~ 2MB (下位32bit, 上位32bit)
    .long 0x00200000 + 0x083, 0    # 2MB ~ 4MB
    .long 0x00400000 + 0x083, 0    # 4MB ~ 6MB
    .fill 509, 8, 0               # 残りのエントリー（計1GB分をカバー）

# -----------------------------------------------------------------------------
# 64ビット用 GDT (グローバル記述子テーブル)
# -----------------------------------------------------------------------------
.align 8
gdt_start:
    .long 0, 0                     # ヌル記述子 (8バイト分を0クリア)
    # 0x08: 64ビット コードセグメント
    .long 0x00000000, 0x00209A00   # 下位32bit, 上位32bit
    # 0x10: 64ビット データセグメント
    .long 0x00000000, 0x00009200   
gdt_end:

# `lgdt` 命令に渡すためのGDTポインタ構造体
.global gdt_pointer
gdt_pointer:
    .word gdt_end - gdt_start - 1  # GDTのサイズ
    .long gdt_start                # GDTの開始物理アドレス（32ビットで収まる位置）
